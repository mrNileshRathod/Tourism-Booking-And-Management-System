    private void createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createActionPerformed
        if(checkData()){
            try {
            String sql = "INSERT INTO clientlogindata(TourId,UserId,Password) VALUES (?,?,?) ";
            con = db.Connect.getConnection();
            pst = con.prepareStatement(sql);
            pst.setString(1, tid.getText());
            pst.setString(2, userid.getText());
            pst.setString(3, password.getText());
            int response =  JOptionPane.showConfirmDialog(this, "Are you sure to create an account ?","Confirm", JOptionPane.YES_NO_OPTION);
                                   
                if(response ==  JOptionPane.YES_OPTION){  
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null,"Account Created!");
                    this.dispose();
                    takeTime(); 
                    T.setInitialDelay(0);
                    T.start();
                }
            }
            catch(ClassNotFoundException | SQLException e){
              JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_createActionPerformed

