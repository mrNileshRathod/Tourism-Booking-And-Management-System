    @SuppressWarnings("unchecked")
    private void autoid() {
        String id = "TUR001";
        try {
            con = db.Connect.getConnection();
            pst = con.prepareStatement("select MAX(TourID) from clientlogindata");
            
            ResultSet rs = pst.executeQuery();
            rs.next();
            
            rs.getString("MAX(TourID)");   
            
            if(rs.getString("MAX(TourID)") == null){
                tid.setText(id);
            }
            else{                
                Long idd = Long.parseLong(rs.getString("MAX(TourID)").substring(3,rs.getString("MAX(TourID)").length()));
                idd++;
                tid.setText("TUR"+String.format("%03d", idd));
            }
        }
        catch(ClassNotFoundException | NumberFormatException | SQLException e){
         JOptionPane.showMessageDialog(null,e);
        }
    }
