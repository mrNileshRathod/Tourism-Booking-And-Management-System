    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        try{
            String id = tid.getText();
            String sql = "DELETE FROM bookingdata WHERE TourId=?";
            con = db.Connect.getConnection();
            pst = con.prepareStatement(sql);
            pst.setString(1, id);
            int response =  JOptionPane.showConfirmDialog(this, "Are you sure to Delete a Booked Package ?","Confirm", JOptionPane.YES_NO_OPTION);
            if(response ==  JOptionPane.YES_OPTION)
            {
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null,"Delete Sucessfully !");
                tot_person.setText("");
                SelectPackage.setSelectedIndex(0);
                price.setText("");
            }
        }
        catch(ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_DeleteActionPerformed

    private void BookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BookActionPerformed
       if(checkData()){
            try{
                String id = tid.getText();
                String pack = (String)SelectPackage.getItemAt(SelectPackage.getSelectedIndex());
                String sql = "INSERT INTO bookingdata(TourId,UserId,PhoneNo,PackageName,TotalPerson,PackagePrice) VALUES (?,?,?,?,?,?)";
                con = db.Connect.getConnection();
                pst = con.prepareStatement(sql);
                
                pst.setString(1, tid.getText());
                pst.setString(2, userid.getText());
                pst.setString(3, PhoneNo.getText());
                pst.setString(4, pack);
                pst.setString(5, tot_person.getText());
                pst.setString(6, price.getText());
                
                int response =  JOptionPane.showConfirmDialog(this, "Are you sure to Book the Package ?","Confirm", JOptionPane.YES_NO_OPTION);
                if(response ==  JOptionPane.YES_OPTION)
                {
                    pst.executeUpdate();
                    tot_person.enable(false);
                    JOptionPane.showMessageDialog(null,"Package Booked Sucessfully !");
                }
            }
            catch(ClassNotFoundException | SQLException e){
                JOptionPane.showMessageDialog(null,"You Already Booked a Package");
            }
        }
    }//GEN-LAST:event_BookActionPerformed

    private void CheckPriceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckPriceActionPerformed
        String t = tot_person.getText();
        int totPerson = Integer.parseInt(t);
        int p;
        if("Platinum".equals(SelectPackage.getItemAt(SelectPackage.getSelectedIndex()))){
            p=totPerson*16000;
            price.setText(Integer.toString(p));
        }
        else if("Gold".equals(SelectPackage.getItemAt(SelectPackage.getSelectedIndex()))){
            p=totPerson*10000;
            price.setText(Integer.toString(p));
        }
        else if("Silver".equals(SelectPackage.getItemAt(SelectPackage.getSelectedIndex()))){
            p=totPerson*6000;
            price.setText(Integer.toString(p));
        }
        else{
            price.setText("");
        }
    }//GEN-LAST:event_CheckPriceActionPerformed

