    private void getLoginID(){
        try{
            userid.setText(LoginedUserName);
            String sql = "SELECT TourId,UserId FROM clientlogindata WHERE UserId = ?";
            con = db.Connect.getConnection();
            pst = con.prepareStatement(sql);
            pst.setString(1, LoginedUserName);
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                tid.setText(rs.getString(1));
                userid.setText(rs.getString(2));
            }
        }
        catch(ClassNotFoundException | SQLException  ex){
           JOptionPane.showMessageDialog(null, ex);
        }
    }
    private void getPersonalInfo(){
        try{
            String sql = "SELECT PhoneNo FROM clientregistrationdata WHERE TourId = ?";
            con = db.Connect.getConnection();
            pst = con.prepareStatement(sql);
            pst.setString(1, tid.getText());
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                PhoneNo.setText(rs.getString(1));
            }
        }
        catch(ClassNotFoundException | SQLException  ex){
           JOptionPane.showMessageDialog(null, ex);
        }
    }
    private void getPackageInfo(){
        try{
            String sql = "SELECT PackageName,TotalPerson,PackagePrice FROM bookingdata WHERE TourId = ?";
            con = db.Connect.getConnection();
            pst = con.prepareStatement(sql);
            pst.setString(1, tid.getText());
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                SelectPackage.setSelectedItem(rs.getString(1));
                tot_person.setText(rs.getString(2));
                price.setText(rs.getString(3));
            }
        }
        catch(ClassNotFoundException | SQLException  ex){
           JOptionPane.showMessageDialog(null, ex);
        }
    }
    private boolean checkData(){
        if(price.getText().equals("") && tot_person.getText().equals("") && PhoneNo.getText().equals("")
                && "Select".equals(SelectPackage.getItemAt(SelectPackage.getSelectedIndex()))){
            JOptionPane.showMessageDialog(null,"Please enter all Infromation !");
            return false;
        }
        else{
            return true;
        }
    }
